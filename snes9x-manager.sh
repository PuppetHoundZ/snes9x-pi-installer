# =============================================================================
# UNINSTALL
# =============================================================================
do_uninstall() {
    step "Uninstalling snes9x"
    _remove_stale_build_dir
    rm -f "$PARTIAL_MARKER"
    _remove_installed_files
    [[ -d "$STATE_DIR" ]] && rm -rf "$STATE_DIR" && info "Removed: $STATE_DIR"

    # ── Config file (snes9x.conf) ─────────────────────────────────────────────
    # This is just preferences — display mode, audio driver, input bindings.
    # Safe to delete; snes9x regenerates it on next launch.
    echo ""
    if [[ -f "$SNES9X_CONF" ]]; then
        echo "  Config file: $SNES9X_CONF"
        echo "  Contains: display/audio/input preferences. Regenerated automatically."
        read -rp "$(echo -e "${CYAN}  Remove snes9x.conf (settings only)? [y/N]: ${NC}")" RM_CONF || true
        if [[ "$RM_CONF" =~ ^[Yy]$ ]]; then
            rm -f "$SNES9X_CONF" "${SNES9X_CONF}.bak"
            info "Config file removed."
        else
            info "Config kept: $SNES9X_CONF"
        fi
    else
        print_info "No snes9x.conf found — nothing to remove."
    fi

    # ── Battery saves (.srm files) ────────────────────────────────────────────
    # These are your in-game progress — IRREPLACEABLE. Default: keep.
    echo ""
    local SRM_COUNT=0
    SRM_COUNT=$(find "$CONFIG_DIR" -maxdepth 2 -name '*.srm' 2>/dev/null | wc -l)
    if [[ $SRM_COUNT -gt 0 ]]; then
        echo "  Battery saves (.srm): $SRM_COUNT file(s) found in $CONFIG_DIR"
        warn "These contain your in-game progress and CANNOT be recovered if deleted."
        read -rp "$(echo -e "${CYAN}  Remove battery saves (.srm)? [y/N]: ${NC}")" RM_SRM || true
        if [[ "$RM_SRM" =~ ^[Yy]$ ]]; then
            find "$CONFIG_DIR" -maxdepth 2 -name '*.srm' -delete
            info "Battery saves removed."
        else
            info "Battery saves kept."
        fi
    else
        print_info "No battery saves (.srm) found."
    fi

    # ── Save states ────────────────────────────────────────────────────────────
    # These are snapshot saves (not in-game saves). Default: keep.
    echo ""
    local STATE_COUNT=0
    STATE_COUNT=$(find "$CONFIG_DIR" -maxdepth 2         \( -name '*.sst' -o -name '*.oops' -o -name '*.undo'            -o -name '*.000' -o -name '*.001' -o -name '*.002'            -o -name '*.003' -o -name '*.004' -o -name '*.005'            -o -name '*.006' -o -name '*.007' -o -name '*.008'            -o -name '*.009' \) 2>/dev/null | wc -l)
    if [[ $STATE_COUNT -gt 0 ]]; then
        echo "  Save states: $STATE_COUNT file(s) found in $CONFIG_DIR"
        read -rp "$(echo -e "${CYAN}  Remove save states? [y/N]: ${NC}")" RM_STATES || true
        if [[ "$RM_STATES" =~ ^[Yy]$ ]]; then
            find "$CONFIG_DIR" -maxdepth 2                 \( -name '*.sst' -o -name '*.oops' -o -name '*.undo'                    -o -name '*.000' -o -name '*.001' -o -name '*.002'                    -o -name '*.003' -o -name '*.004' -o -name '*.005'                    -o -name '*.006' -o -name '*.007' -o -name '*.008'                    -o -name '*.009' \) -delete 2>/dev/null || true
            info "Save states removed."
        else
            info "Save states kept."
        fi
    else
        print_info "No save states found."
    fi

    # ── Remove config directory if now empty ──────────────────────────────────
    if [[ -d "$CONFIG_DIR" ]]; then
        local REMAINING
        REMAINING=$(find "$CONFIG_DIR" -not -path "$STATE_DIR*"             -not -name '.*' -mindepth 1 2>/dev/null | wc -l)
        if [[ $REMAINING -eq 0 ]]; then
            rm -rf "$CONFIG_DIR"
            info "Config directory empty — removed: $CONFIG_DIR"
        else
            print_info "Config directory kept (contains files): $CONFIG_DIR"
        fi
    fi

    echo ""
    [[ -d "$ROM_DIR" ]] && print_info "Your ROMs are untouched: $ROM_DIR"

    # Build + runtime deps are retained — other system packages may depend on them.
    # To remove manually later:
    #   sudo apt-get remove build-essential cmake libgtkmm-3.0-dev libsdl2-dev \
    #       libx11-dev libxext-dev libxv-dev libxrandr-dev libwayland-dev \
    #       wayland-protocols libepoxy-dev libpulse-dev libasound2-dev \
    #       portaudio19-dev libminizip-dev zlib1g-dev libpng-dev gettext
    info "Dependencies retained (safe to leave; remove manually if needed)."

    echo ""
    divider
    echo -e "${GREEN}  snes9x fully uninstalled.${NC}"
    divider
    echo ""
    exit 0
}

