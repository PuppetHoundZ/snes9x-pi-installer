# MAIN MENU
# =============================================================================
main_menu() {
    mkdir -p "$STATE_DIR"
    _check_partial_state

    while true; do
        echo ""
        divider
        echo -e "${BOLD}  SNES9X Manager — Raspberry Pi 4${NC}"
        echo -e "  snes9x-gtk ${SNES9X_TAG} | Pi OS Trixie arm64 | Wayland + OpenGL + PipeWire"
        echo -e "  Source: https://github.com/snes9xgit/snes9x"
        divider
        echo ""
        snes9x_status
        echo ""
        echo -e "  ${CYAN}1)${NC}  Install"
        echo -e "  ${CYAN}2)${NC}  Update  (fresh rebuild — stable tag or master)"
        echo -e "  ${CYAN}3)${NC}  Performance & display guide"
        echo -e "  ${CYAN}4)${NC}  Diagnostics"
        echo -e "  ${CYAN}5)${NC}  Edit snes9x.conf manually"
        echo -e "  ${CYAN}6)${NC}  Uninstall  (keeps dependencies, saves prompt, ROMs safe)"
        echo -e "  ${CYAN}7)${NC}  Exit"
        echo ""
        read -rp "$(echo -e "${CYAN}  Choose an option [1-7]: ${NC}")" CHOICE || true

        case "$CHOICE" in
            1) do_install ;;
            2) do_update  ;;
            3) performance_guide ;;
            4)
                echo ""
                divider
                echo -e "${BOLD}  Diagnostics${NC}"
                divider
                echo ""
                if [[ ! -f "$SNES9X_CONF" ]]; then
                    warn "snes9x.conf not found — run Install first."
                else
                    print_ok "snes9x.conf found at: $SNES9X_CONF"
                    echo ""
                    print_info "Frame Skip:"
                    grep -E '^\s*FrameSkip' "$SNES9X_CONF" 2>/dev/null \
                        | sed 's/^/    /' || echo "    (keys not yet written — launch snes9x once)"
                    echo ""
                    print_info "Audio driver (1=PulseAudio/PipeWire):"
                    grep -E '^\s*SoundDriver' "$SNES9X_CONF" 2>/dev/null \
                        | sed 's/^/    /' || echo "    (not configured)"
                    echo ""
                    print_info "Controller blocks:"
                    grep -E '^\s*Joypad[0-9]' "$SNES9X_CONF" 2>/dev/null \
                        | head -20 | sed 's/^/    /' || echo "    (none configured)"
                fi
                echo ""
                read -rp "$(echo -e "${CYAN}  Press Enter to continue… ${NC}")" _ || true
                ;;
            5)
                if [[ -f "$SNES9X_CONF" ]]; then
                    "${EDITOR:-nano}" "$SNES9X_CONF" 2>/dev/null || \
                        nano "$SNES9X_CONF" 2>/dev/null || \
                        vi   "$SNES9X_CONF"
                else
                    warn "snes9x.conf not found — run Install first."
                fi
                ;;
            6) do_uninstall ;;
            7)
                echo ""
                echo "  Goodbye. Enjoy Star Fox 2."
                echo ""
                exit 0
                ;;
            *) warn "Invalid choice — enter 1–7." ;;
        esac
    done
}

main_menu
